--Customer Table creation
declare 
v_cnt number;
begin
    select count(*) into v_cnt from user_tables where table_name = upper('customer');
    if v_cnt = 0 then
        execute immediate('
            create table customer (
            customer_id int generated by DEFAULT as IDENTITY (start with 1) not null primary key,
            first_name varchar(50) not null,
            last_name varchar(50) not null,
            date_of_birth DATE,
            phone_number number(10) not null,
            email varchar(50) not null,
            status varchar(1) default 1,
            pass_hash varchar(128),
            update_timestamp timestamp default systimestamp)');
        dbms_output.put_line('Customer table created');
        
    else
    execute immediate ('delete from customer');
        dbms_output.put_line('Customer table already exists');
    end if;
end;
/

grant references on customer to item_owner, order_owner;
grant select on customer to item_owner, order_owner;

--Customer Address Table creation
declare 
v_cnt number;
begin
    select count(*) into v_cnt from user_tables where table_name = upper('customer_address');
    if v_cnt = 0 then
        execute immediate('
            create table customer_address (
            address_id int generated by DEFAULT as IDENTITY (start with 1) not null primary key,
            customer_id number not null,
            FOREIGN KEY(customer_id) REFERENCES customer(customer_id),
            address_type varchar2(20) not null,
            address_line1 varchar(50) not null,
            address_line2 varchar(50),
            city varchar(20) not null,
            state_name varchar(20) not null,
            zip char(5) not null)');
        dbms_output.put_line('customer_address table created');
        
    else
     execute immediate ('delete from customer_address');
        dbms_output.put_line('customer_address table already exists');
    end if;
end;
/

grant references on customer_address to item_owner, order_owner;
grant select on customer_address to order_owner;


